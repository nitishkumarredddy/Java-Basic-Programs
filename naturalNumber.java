
public class naturalNumber
{
	public static void main(String[] args)
	{
		int n = 50;
		int sum = 0;
		int j = 1;
		 while (j<=n)
			 {
			   sum += j;
			   j++;
			 }
		 System.out.println("The sum is = " + sum);
		 
		 int  m = 100;
		 int result = 0;
		 
		 for (int i = 1 ; i <= m ; ++i)
		 {
			 
			 result += i; 
		 }
		 System.out.println(result);
		 
	}
}
